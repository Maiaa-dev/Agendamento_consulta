#self é equivalente ao this em c#
class Paciente:
    def __init__(self, id_paciente, nome, sobrenome, idade, sexo, telefone, data_nasc, email):
        self.id_paciente = id_paciente
        self.nome = nome
        self.sobrenome = sobrenome
        self.idade = idade
        self.sexo = sexo
        self.telefone = telefone
        self.data_nasc = data_nasc
        self.email = email



